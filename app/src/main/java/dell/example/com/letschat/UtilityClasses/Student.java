package dell.example.com.letschat.UtilityClasses;



public class Student {
    String sname;
    String sid;





    public Student(String sname, String sid) {
        this.sname = sname;
        this.sid = sid;


    }



    public String getSname()
    { return sname; }

    public String getSid() {
        return sid;
    }



}
